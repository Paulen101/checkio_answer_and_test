turtle.shape('triangle')    
turtle.color('red')
turtle.pensize(5)
turtle.speed(1)
turtle.begin_fill()
turtle.forward(100)
turtle.left(120)
turtle.forward(100)
turtle.left(120)
turtle.forward(100)
turtle.left(120)
turtle.color('blue')
turtle.end_fill()
turtle.exitonclick()
